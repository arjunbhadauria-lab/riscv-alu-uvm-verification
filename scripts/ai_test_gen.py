# =============================================================================
# ALU Reference Model (mirrors the RTL exactly)
# =============================================================================

ALU_OPS = {
    0: "ADD", 1: "SUB", 2: "AND", 3: "OR", 4: "XOR",
    5: "SLL", 6: "SRL", 7: "SRA", 8: "SLT", 9: "SLTU"
}

MASK_32 = 0xFFFFFFFF
MASK_33 = 0x1FFFFFFFF


def to_signed_32(val):
    val &= MASK_32
    return val - 0x100000000 if val >= 0x80000000 else val


def alu_compute(op, a, b):
    a &= MASK_32
    b &= MASK_32
    add_res = (a + b) & MASK_33
    sub_res = (a - b) & MASK_33

    if op == 0:
        result = add_res & MASK_32
    elif op == 1:
        result = sub_res & MASK_32
    elif op == 2:
        result = a & b
    elif op == 3:
        result = a | b
    elif op == 4:
        result = a ^ b
    elif op == 5:
        result = (a << (b & 0x1F)) & MASK_32
    elif op == 6:
        result = (a >> (b & 0x1F)) & MASK_32
    elif op == 7:
        result = (to_signed_32(a) >> (b & 0x1F)) & MASK_32
    elif op == 8:
        result = 1 if to_signed_32(a) < to_signed_32(b) else 0
    elif op == 9:
        result = 1 if a < b else 0
    else:
        result = 0

    zero = int(result == 0)
    negative = int((result >> 31) & 1)
    carry = int((add_res >> 32) & 1) if op == 0 else int((sub_res >> 32) & 1) if op == 1 else 0

    if op == 0:
        overflow = int(((a >> 31) == (b >> 31)) and (((add_res >> 31) & 1) != ((a >> 31) & 1)))
    elif op == 1:
        overflow = int(((a >> 31) != (b >> 31)) and (((sub_res >> 31) & 1) != ((a >> 31) & 1)))
    else:
        overflow = 0

    return result, zero, overflow, carry, negative


# =============================================================================
# Test Vector Data Class
# =============================================================================

@dataclass
class TestVector:
    operand_a: int
    operand_b: int
    alu_op: int
    description: str = ""

    def verify(self):
        result, zero, overflow, carry, negative = alu_compute(
            self.alu_op, self.operand_a, self.operand_b
        )
        return {
            "op_a": f"0x{self.operand_a:08X}",
            "op_b": f"0x{self.operand_b:08X}",
            "op": ALU_OPS.get(self.alu_op, "UNKNOWN"),
            "result": f"0x{result:08X}",
            "zero": zero, "overflow": overflow,
            "carry": carry, "negative": negative,
            "description": self.description
        }


# =============================================================================
# Coverage Tracker
# =============================================================================

class CoverageTracker:
    def __init__(self):
        self.op_bins = {i: False for i in range(10)}
        boundary_vals = ["zero", "max_pos", "min_neg", "all_ones", "one", "positive", "negative"]
        self.opa_bins = {v: False for v in boundary_vals}
        self.opb_bins = {v: False for v in boundary_vals}
        self.flag_bins = {
            "zero_0": False, "zero_1": False,
            "overflow_0": False, "overflow_1": False,
            "carry_0": False, "carry_1": False,
            "negative_0": False, "negative_1": False
        }
        self.op_x_opa = {}
        self.op_x_opb = {}
        for op in range(10):
            for val in boundary_vals:
                self.op_x_opa[(op, val)] = False
                self.op_x_opb[(op, val)] = False

    def _categorise(self, val):
        val &= MASK_32
        if val == 0x00000000: return "zero"
        elif val == 0x7FFFFFFF: return "max_pos"
        elif val == 0x80000000: return "min_neg"
        elif val == 0xFFFFFFFF: return "all_ones"
        elif val == 0x00000001: return "one"
        elif val <= 0x7FFFFFFE: return "positive"
        else: return "negative"

    def sample(self, tv):
        result, zero, overflow, carry, negative = alu_compute(tv.alu_op, tv.operand_a, tv.operand_b)
        if tv.alu_op in self.op_bins:
            self.op_bins[tv.alu_op] = True
        cat_a = self._categorise(tv.operand_a)
        cat_b = self._categorise(tv.operand_b)
        self.opa_bins[cat_a] = True
        self.opb_bins[cat_b] = True
        self.flag_bins[f"zero_{zero}"] = True
        self.flag_bins[f"overflow_{overflow}"] = True
        self.flag_bins[f"carry_{carry}"] = True
        self.flag_bins[f"negative_{negative}"] = True
        if tv.alu_op in range(10):
            self.op_x_opa[(tv.alu_op, cat_a)] = True
            self.op_x_opb[(tv.alu_op, cat_b)] = True

    def get_report(self):
        def pct(d):
            return (sum(d.values()) / len(d)) * 100 if d else 0
        return {
            "operation_coverage": pct(self.op_bins),
            "operand_a_coverage": pct(self.opa_bins),
            "operand_b_coverage": pct(self.opb_bins),
            "flag_coverage": pct(self.flag_bins),
            "cross_op_x_opa": pct(self.op_x_opa),
            "cross_op_x_opb": pct(self.op_x_opb),
            "op_bins_hit": f"{sum(self.op_bins.values())}/10",
            "opa_bins_hit": f"{sum(self.opa_bins.values())}/7",
            "opb_bins_hit": f"{sum(self.opb_bins.values())}/7",
            "cross_opa_hit": f"{sum(self.op_x_opa.values())}/70",
            "cross_opb_hit": f"{sum(self.op_x_opb.values())}/70",
        }


