def export_sv_stimulus(tests, filename="ai_stimulus.sv"):
    filepath = os.path.join(os.path.dirname(os.path.abspath(__file__)), filename)
    with open(filepath, "w") as f:
        f.write("// Auto-generated AI test stimulus for RISC-V ALU\n")
        f.write(f"// {len(tests)} test vectors generated by Claude (Anthropic)\n")
        f.write("// These can be included in a UVM sequence body() task\n\n")
        for i, tv in enumerate(tests):
            f.write(f"// Test {i+1}: {tv.description}\n")
            f.write(f"item = alu_seq_item::type_id::create(\"item\");\n")
            f.write(f"start_item(item);\n")
            f.write(f"item.operand_a = 32'h{tv.operand_a:08X};\n")
            f.write(f"item.operand_b = 32'h{tv.operand_b:08X};\n")
            f.write(f"item.alu_op    = 4'd{tv.alu_op};\n")
            f.write(f"finish_item(item);\n\n")
    print(f"Exported SystemVerilog stimulus to: {filepath}")


