ALU_SPEC_PROMPT = """You are a hardware verification engineer. Generate test vectors for a 32-bit RISC-V RV32I ALU.

The ALU supports these operations:
- 0: ADD (32-bit addition)
- 1: SUB (32-bit subtraction)
- 2: AND (bitwise AND)
- 3: OR (bitwise OR)
- 4: XOR (bitwise XOR)
- 5: SLL (shift left logical, shift amount = operand_b[4:0])
- 6: SRL (shift right logical, shift amount = operand_b[4:0])
- 7: SRA (shift right arithmetic, shift amount = operand_b[4:0])
- 8: SLT (set less than, signed comparison)
- 9: SLTU (set less than, unsigned comparison)

The ALU generates flags: zero, carry (ADD/SUB only), overflow (signed, ADD/SUB only), negative.

Generate {num_tests} test vectors that maximise functional coverage. Focus on:
- Boundary values: 0x00000000, 0xFFFFFFFF, 0x7FFFFFFF, 0x80000000, 0x00000001
- Signed overflow and underflow conditions
- Carry generation scenarios
- Shift boundary cases (shift by 0, 1, 31)
- Signed vs unsigned comparison differences (SLT vs SLTU)
- Each ALU operation should be tested with different operand categories

Respond ONLY with a JSON array. No other text, no markdown backticks. Each element must have:
- "operand_a": integer (decimal)
- "operand_b": integer (decimal)
- "alu_op": integer (0-9)
- "description": short string explaining what the test targets

Example format:
[{{"operand_a": 2147483647, "operand_b": 1, "alu_op": 0, "description": "ADD: max positive + 1, triggers signed overflow"}}]
"""


def generate_live_ai_tests(num_tests=50):
    try:
        import google.generativeai as genai
    except ImportError:
        print("Error: google-generativeai package not installed.")
        print("Run: python -m pip install google-generativeai")
        sys.exit(1)

    api_key = os.environ.get("GEMINI_API_KEY")
    if not api_key:
        print("Error: GEMINI_API_KEY environment variable not set.")
        print("Get a free key from https://aistudio.google.com/apikey")
        print("Then run: $env:GEMINI_API_KEY='your-key-here'")
        sys.exit(1)

    genai.configure(api_key=api_key)
    model = genai.GenerativeModel("gemini-2.0-flash")
    print(f"Requesting {num_tests} test vectors from Gemini API...")

    response = model.generate_content(ALU_SPEC_PROMPT.format(num_tests=num_tests))
    response_text = response.text.strip()
    if response_text.startswith("```"):
        response_text = response_text.split("\n", 1)[1]
        response_text = response_text.rsplit("```", 1)[0]

    try:
        raw_tests = json.loads(response_text)
    except json.JSONDecodeError as e:
        print(f"Error parsing AI response: {e}")
        sys.exit(1)

    tests = []
    for t in raw_tests:
        try:
            tv = TestVector(
                operand_a=int(t["operand_a"]) & MASK_32,
                operand_b=int(t["operand_b"]) & MASK_32,
                alu_op=int(t["alu_op"]),
                description=t.get("description", "")
            )
            if 0 <= tv.alu_op <= 9:
                tests.append(tv)
        except (KeyError, ValueError) as e:
            print(f"  Skipping malformed test: {e}")

    print(f"Successfully parsed {len(tests)} test vectors from Gemini API.\n")
    return tests


