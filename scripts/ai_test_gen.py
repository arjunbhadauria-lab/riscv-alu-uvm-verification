def run_comparison(ai_tests, random_tests):
    print("=" * 70)
    print("  AI vs CONSTRAINED RANDOM — COVERAGE COMPARISON")
    print("=" * 70)

    ai_cov = CoverageTracker()
    for tv in ai_tests:
        ai_cov.sample(tv)
    ai_report = ai_cov.get_report()

    rnd_cov = CoverageTracker()
    for tv in random_tests:
        rnd_cov.sample(tv)
    rnd_report = rnd_cov.get_report()

    print(f"\n{'Metric':<30} {'AI (' + str(len(ai_tests)) + ' tests)':<25} {'Random (' + str(len(random_tests)) + ' tests)':<25}")
    print("-" * 80)

    metrics = [
        ("Operation Coverage", "operation_coverage", "op_bins_hit"),
        ("Operand A Coverage", "operand_a_coverage", "opa_bins_hit"),
        ("Operand B Coverage", "operand_b_coverage", "opb_bins_hit"),
        ("Flag Coverage", "flag_coverage", None),
        ("Cross: Op x OpA", "cross_op_x_opa", "cross_opa_hit"),
        ("Cross: Op x OpB", "cross_op_x_opb", "cross_opb_hit"),
    ]

    for label, pct_key, bin_key in metrics:
        ai_pct = ai_report[pct_key]
        rnd_pct = rnd_report[pct_key]
        if bin_key:
            ai_str = f"{ai_pct:6.1f}%  ({ai_report[bin_key]})"
            rnd_str = f"{rnd_pct:6.1f}%  ({rnd_report[bin_key]})"
        else:
            ai_str = f"{ai_pct:6.1f}%"
            rnd_str = f"{rnd_pct:6.1f}%"
        winner = " <-- AI" if ai_pct > rnd_pct else " <-- Random" if rnd_pct > ai_pct else " (tie)"
        print(f"{label:<30} {ai_str:<25} {rnd_str:<25}{winner}")

    # AI test details
    print(f"\n{'=' * 70}")
    print("  AI-GENERATED TEST DETAILS")
    print("=" * 70)

    for i, tv in enumerate(ai_tests):
        v = tv.verify()
        print(f"\n  Test {i+1}: {v['description']}")
        print(f"    {v['op']}: A={v['op_a']}, B={v['op_b']} -> Result={v['result']}")
        print(f"    Flags: zero={v['zero']}, overflow={v['overflow']}, carry={v['carry']}, negative={v['negative']}")

    # Summary
    print(f"\n{'=' * 70}")
    print("  ANALYSIS SUMMARY")
    print("=" * 70)

    ai_cross = (ai_report["cross_op_x_opa"] + ai_report["cross_op_x_opb"]) / 2
    rnd_cross = (rnd_report["cross_op_x_opa"] + rnd_report["cross_op_x_opb"]) / 2

    print(f"\n  AI average cross coverage:     {ai_cross:.1f}%")
    print(f"  Random average cross coverage: {rnd_cross:.1f}%")

    if ai_cross > rnd_cross:
        print(f"\n  AI-generated tests achieved {ai_cross - rnd_cross:.1f}% higher cross coverage")
        print(f"  with {len(ai_tests)} tests vs {len(random_tests)} random tests.")
        print(f"  This demonstrates that targeted AI stimulus can reach coverage")
        print(f"  goals more efficiently than purely random approaches.")
    elif rnd_cross > ai_cross:
        print(f"\n  Random tests achieved {rnd_cross - ai_cross:.1f}% higher cross coverage.")
        print(f"  Constrained random naturally explores the design space broadly,")
        print(f"  while AI may over-focus on known corner cases.")
    else:
        print(f"\n  Both approaches achieved equal cross coverage.")

    print(f"\n  Conclusion: The most effective verification combines AI-targeted")
    print(f"  corner cases with constrained random breadth — which is exactly")
    print(f"  what the alu_full_test does by running directed tests first,")
    print(f"  then flooding with random stimulus.\n")

    return ai_report, rnd_report


