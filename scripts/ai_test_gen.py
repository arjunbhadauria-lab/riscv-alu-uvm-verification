# Random Test Generator (mirrors UVM constrained random)
# =============================================================================

def generate_random_tests(num_tests=50):
    tests = []
    for _ in range(num_tests):
        r = random.random()
        if r < 0.05:     a = 0x00000000
        elif r < 0.10:   a = 0xFFFFFFFF
        elif r < 0.15:   a = 0x80000000
        elif r < 0.20:   a = 0x7FFFFFFF
        elif r < 0.60:   a = random.randint(0x00000001, 0x7FFFFFFE)
        else:             a = random.randint(0x80000001, 0xFFFFFFFE)

        r = random.random()
        if r < 0.05:     b = 0x00000000
        elif r < 0.10:   b = 0xFFFFFFFF
        elif r < 0.15:   b = 0x80000000
        elif r < 0.20:   b = 0x7FFFFFFF
        elif r < 0.60:   b = random.randint(0x00000001, 0x7FFFFFFE)
        else:             b = random.randint(0x80000001, 0xFFFFFFFE)

        op = random.randint(0, 9)
        tests.append(TestVector(a, b, op, "constrained random"))
    return tests


